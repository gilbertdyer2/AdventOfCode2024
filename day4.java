    public static int findXmas() {
        int total = 0;
        // Horizontal
        // Backwards Horizontal
        String[] horizontal = input.split("\n");
        for (int i = 0; i < horizontal.length; i++) {
            total += getXmasCount(horizontal[i]);
            total += getXmasCount(new StringBuffer(horizontal[i]).reverse().toString());
        }
        // Vertical
        // Backwards Vertical
        String[] vertical = getVertical();
        for (int i = 0; i < vertical.length; i++) {
            total += getXmasCount(vertical[i]);
            total += getXmasCount(new StringBuffer(vertical[i]).reverse().toString());
        }

        // Diagonal topLeft->bottomRight
        // Backwards Diagonal topLeft->bottomRight
        String[] diagTopLeft = getDiagonalTopLeftBotRight();
        for (int i = 0; i < diagTopLeft.length; i++) {
            total += getXmasCount(diagTopLeft[i]);
            total += getXmasCount(new StringBuffer(diagTopLeft[i]).reverse().toString());
        }

        // Diagonal bottomLeft->topRight
        // Backwards Diagonal bottomLeft->topRight
        String[] diagBotLeft = getDiagonalBotLeftTopRight();
        for (int i = 0; i < diagBotLeft.length; i++) {
            total += getXmasCount(diagBotLeft[i]);
            total += getXmasCount(new StringBuffer(diagBotLeft[i]).reverse().toString());
        }

        return total;
    }

    private static int getXmasCount(String inp) {
        int count = 0;
        int j = 0;
        while (inp.indexOf("XMAS", j) != -1) {
            count++;

            j = inp.indexOf("XMAS", j) + 1;
        }

        return count;
    }

    private static String[] getVertical() {
        String[] rows = input.split("\n");
        String[] cols = new String[rows[0].length()];
        
        for (int i = 0; i < cols.length; i++) {
           cols[i] = "";
        }
        
        for (int i = 0; i < rows[0].length(); i++) {
            for (int j = 0; j < rows.length; j++) {
                cols[i] += rows[j].charAt(i);
            }
        }
        System.out.println(cols[0]);

        return cols;
    }

    private static String[] getDiagonalTopLeftBotRight() {
        if (rows == null) 
            initRows();

        String[] diagStrings = new String[rows[0].length + rows.length - 1]; 
        // start from last row
        int curIdx = 0;

        String diag = "";
        for (int i = rows.length - 1; i >= 0; i--) {
            

            int curCol = 0;
            int curRow = i;

            // Traverse down-right
            while (curCol < rows[0].length && curRow < rows.length) {
                diag += rows[curRow][curCol];

                curRow++;
                curCol++;
                
            }
            diagStrings[curIdx] = diag;

            curIdx++;
            diag = "";
        }

        // start from 2nd col
        for (int i = 1; i < rows[0].length; i++) {
            int curCol = i;
            int curRow = 0;

            // Traverse down-right
            while (curCol < rows[0].length && curRow < rows.length) {
                diag += rows[curRow][curCol];

                curRow++;
                curCol++;
                
            }
            diagStrings[curIdx] = diag;

            curIdx++;
            diag = "";
        }
        // for (String s : diagStrings)
        //     System.out.println(s);

        return diagStrings;
    
    }

    private static String[] getDiagonalBotLeftTopRight() {
        if (rows == null) 
            initRows();

        String[] diagStrings = new String[rows[0].length + rows.length - 1]; 

        // start from first row
        int curIdx = 0;

        String diag = "";
        for (int i = 0; i < rows.length; i++) {
            int curCol = 0;
            int curRow = i;

            // Traverse up-right
            while (curCol < rows.length && curRow >= 0) {
                diag += rows[curRow][curCol];

                curRow--;
                curCol++;
                
            }
            diagStrings[curIdx] = diag;

            curIdx++;
            diag = "";
        }

        // start from 2nd col, last row
        for (int i = 1; i < rows[0].length; i++) {
            int curCol = i;
            int curRow = rows.length - 1;

            // Traverse up-right
            while (curCol < rows[0].length && curRow >= 0) {
                diag += rows[curRow][curCol];

                curRow--;
                curCol++;
                
            }
            diagStrings[curIdx] = diag;

            curIdx++;
            diag = "";
        }
        // for (String s : diagStrings)
        //     System.out.println(s);

        return diagStrings;
    
    }

